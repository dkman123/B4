CREATE TABLE IF NOT EXISTS `%s` (
  `id` INT UNSIGNED NOT NULL AUTO_INCREMENT,
  `client_id` INT UNSIGNED NOT NULL DEFAULT '0',
  `kills` MEDIUMINT UNSIGNED NOT NULL DEFAULT '0',
  `deaths` MEDIUMINT UNSIGNED NOT NULL DEFAULT '0',
  `teamkills` SMALLINT UNSIGNED NOT NULL DEFAULT '0',
  `teamdeaths` SMALLINT UNSIGNED NOT NULL DEFAULT '0',
  `suicides` SMALLINT UNSIGNED NOT NULL DEFAULT '0',
  `ratio` FLOAT NOT NULL DEFAULT '0',
  `skill` FLOAT NOT NULL DEFAULT '0',
  `assists` MEDIUMINT NOT NULL DEFAULT '0',
  `assistskill` FLOAT NOT NULL DEFAULT '0',
  `curstreak` SMALLINT NOT NULL DEFAULT '0',
  `winstreak` SMALLINT NOT NULL DEFAULT '0',
  `losestreak` SMALLINT NOT NULL DEFAULT '0',
  `rounds` SMALLINT UNSIGNED NOT NULL DEFAULT '0',
  `hide` TINYINT NOT NULL DEFAULT '0',
  `fixed_name` VARCHAR(32) NOT NULL DEFAULT '',
  `id_token` VARCHAR(10) NOT NULL DEFAULT '',
  PRIMARY KEY (`id`),
  UNIQUE KEY `client_id` (`client_id`)
) ENGINE=MyISAM DEFAULT CHARSET=utf8;